import mrbpnn
import matplotlib.pyplot as plt
import numpy
import time

def bench(batch_sz, layers):
    init = time.time()
    net = mrbpnn.Network("./data_banknote_authentication.txt", batch_sz, 0.0155, 0.03, 0.9)
    net.add_layer(4, "linear")
    for i in range(layers):
        net.add_layer(5, "relu")
    net.add_layer(1, "resig")
    net.initialize()
    initend = time.time()
    net.train(50)
    end = time.time()
    return (end-init)

x = []
y = []
i = 1
while(i < 1340):
    print(i)
    y.append(bench(i, 1))
    x.append(i)
    i+=1;
plt.plot(x,y, label = "ML-in-Parallel (Sequential)")
    
